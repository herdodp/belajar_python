# casting tipe data pada variable angka ke tipe data yang lain

angka = 0

# integer to float
angkaFloat = float(angka)
print(angkaFloat)
print(type(angkaFloat))

# integer to string
angkaString = str(angka)
print(angkaString)
print(type(angkaString))  

# integer to boolean
angkaBoolean = bool(angka)
print(angkaBoolean)
print(type(angkaBoolean))



 